package codegen

import (
	"quill/ast"
	"quill/stdlib"
	"strings"
)

type Generator struct {
	indent               int
	declared             map[string]bool
	browser              bool
	workerMode           bool     // true when generating Cloudflare Worker output (ES modules)
	expoMode             bool     // true when generating React Native / Expo output (JSX)
	SourceMap            *SourceMap
	genLine              int      // current generated line number (0-based)
	needsResultRuntime   bool     // inject Result type runtime helpers
	needsAIRuntime       bool     // inject Anthropic SDK require and client
	needsOpenAIRuntime   bool     // inject OpenAI provider runtime
	needsGeminiRuntime   bool     // inject Gemini provider runtime
	needsOllamaRuntime   bool     // inject Ollama provider runtime
	needsAgentRuntime    bool     // inject Agent class runtime
	needsVectorRuntime   bool     // inject embedding/vector search runtime
	needsDocumentRuntime   bool     // inject document processing runtime
	needsStructuredOutput  bool     // inject structured output parser
	taggedTemplatesUsed  []string // track which tagged template tags are used
	discordClientVar     string   // tracks the variable name for the Discord client
	importedFiles        map[string]bool // tracks imported .quill files to prevent circular imports
	expoStyleKeys        map[string]bool // tracks known StyleSheet keys for styles. prefix logic
}

func New() *Generator {
	return &Generator{
		declared:      make(map[string]bool),
		importedFiles: make(map[string]bool),
	}
}

func NewBrowser() *Generator {
	return &Generator{
		declared:      make(map[string]bool),
		browser:       true,
		importedFiles: make(map[string]bool),
	}
}

func (g *Generator) Generate(program *ast.Program) string {
	// Expo mode: generate React Native / JSX output
	if g.expoMode {
		return g.generateExpo(program)
	}

	// If the program contains a worker handler, use worker mode (ES modules, no Node.js runtime)
	if g.hasWorkerHandler(program) {
		g.workerMode = true
		return g.generateWorker(program)
	}

	var out strings.Builder

	// Generate user code first to check for await
	// Skip CommandStatements - they are handled separately
	var userCode strings.Builder
	for _, stmt := range program.Statements {
		if _, ok := stmt.(*ast.CommandStatement); ok {
			continue
		}
		userCode.WriteString(g.genStmt(stmt))
		userCode.WriteString("\n")
	}
	userCodeStr := userCode.String()

	// Detect AI feature runtimes from generated code
	if strings.Contains(userCodeStr, "embed(") || strings.Contains(userCodeStr, "createVectorStore(") || strings.Contains(userCodeStr, "cosineSimilarity(") {
		g.needsVectorRuntime = true
	}
	if strings.Contains(userCodeStr, "extract(") || strings.Contains(userCodeStr, "chunk(") || strings.Contains(userCodeStr, "splitSentences(") || strings.Contains(userCodeStr, "splitParagraphs(") {
		g.needsDocumentRuntime = true
	}
	if strings.Contains(userCodeStr, "createAgent(") {
		g.needsAgentRuntime = true
	}

	// Check if we need .env loading (env() calls or Discord code or AI code)
	needsEnvLoader := strings.Contains(userCodeStr, "process.env.") || g.hasCommandStatements(program) || strings.Contains(userCodeStr, "Discord") || g.needsAIRuntime || g.needsOpenAIRuntime || g.needsGeminiRuntime || g.needsOllamaRuntime

	out.WriteString("// Generated by Quill\n")

	// Inject .env loader if needed (Node.js only, not browser)
	if needsEnvLoader && !g.browser {
		out.WriteString("try { require('fs').readFileSync('.env', 'utf-8').split('\\n').forEach(line => { const [key, ...rest] = line.split('='); if (key && rest.length) process.env[key.trim()] = rest.join('=').trim(); }); } catch(e) {}\n")
	}

	// Inject Anthropic AI SDK if needed
	if g.needsAIRuntime {
		out.WriteString("const __anthropic = require(\"@anthropic-ai/sdk\");\n")
		out.WriteString("const __ai_client = new __anthropic.default();\n")
	}

	// Inject multi-provider AI runtimes (also needed for structured output parser)
	if g.needsOpenAIRuntime || g.needsGeminiRuntime || g.needsOllamaRuntime || g.needsStructuredOutput {
		out.WriteString(stdlib.GetAIProvidersRuntime())
		out.WriteString("\n")
	}

	// Inject agent runtime
	if g.needsAgentRuntime {
		out.WriteString(stdlib.GetAgentRuntime())
		out.WriteString("\n")
	}

	// Inject vector/embedding runtime
	if g.needsVectorRuntime {
		out.WriteString(stdlib.GetVectorRuntime())
		out.WriteString("\n")
	}

	// Inject document processing runtime
	if g.needsDocumentRuntime {
		out.WriteString(stdlib.GetDocumentRuntime())
		out.WriteString("\n")
	}

	if g.browser {
		out.WriteString(stdlib.BrowserRuntime)
	} else {
		runtime := stdlib.Runtime
		// If web runtime will be used, remove the basic createServer to avoid duplication
		if strings.Contains(userCodeStr, "createServer(") || strings.Contains(userCodeStr, "createSecureServer(") {
			// Remove the basic HTTP createServer definition from the base runtime
			// It starts with "const createServer = () =>" and ends at the next "const " declaration
			if idx := strings.Index(runtime, "\nconst createServer = ()"); idx != -1 {
				// Find the next top-level const after createServer
				rest := runtime[idx+1:]
				if end := strings.Index(rest[len("const createServer"):], "\nconst "); end != -1 {
					runtime = runtime[:idx+1] + rest[len("const createServer")+end+1:]
				}
			}
		}
		out.WriteString(runtime)
	}
	out.WriteString("\n")

	// Include framework runtime if any component statements are present
	if g.hasComponents(program) {
		out.WriteString(stdlib.FrameworkRuntime)
		out.WriteString("\n")
		// Include utility CSS framework for components
		if g.browser {
			out.WriteString(stdlib.GetUIRuntime())
			out.WriteString("\n")
		}
	}

	// Include template engine runtime if template functions are used
	if strings.Contains(userCodeStr, "tag(") || strings.Contains(userCodeStr, "page(") || strings.Contains(userCodeStr, "escapeHTML(") || strings.Contains(userCodeStr, "layout(") || strings.Contains(userCodeStr, "partial(") || strings.Contains(userCodeStr, "eachItem(") || strings.Contains(userCodeStr, "showIf(") {
		out.WriteString(stdlib.GetTemplateRuntime())
		out.WriteString("\n")
	}

	// Include channel runtime if concurrency features are used
	if g.hasConcurrency(program) {
		out.WriteString(channelRuntime)
		out.WriteString("\n")
	}

	// Include iterator runtime if generator/iterator features are used
	if g.hasGenerators(program) || g.needsIteratorRuntime(userCodeStr) {
		out.WriteString(stdlib.GetIteratorRuntime())
		out.WriteString("\n")
	}

	// Include stdlib runtimes based on usage detection in user code
	if g.usesIdentifier(userCodeStr, "Auth") {
		out.WriteString(stdlib.GetAuthRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "DB") {
		out.WriteString(stdlib.GetOrmRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "Validate") {
		out.WriteString(stdlib.GetValidateRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "Log") {
		out.WriteString(stdlib.GetLoggingRuntime())
		out.WriteString("\n")
	}

	// Include Discord bot runtime if discord-related code is detected
	if g.usesIdentifier(userCodeStr, "createBot") {
		out.WriteString(stdlib.DiscordRuntime)
		out.WriteString("\n")
	}

	// Include web server runtime if Express-related code is detected
	if strings.Contains(userCodeStr, "createServer(") || strings.Contains(userCodeStr, "createSecureServer(") {
		out.WriteString(stdlib.WebRuntime)
		out.WriteString("\n")
	}

	// Include CLI runtime if arg/flag functions are used
	if strings.Contains(userCodeStr, "arg(") || strings.Contains(userCodeStr, "args(") || strings.Contains(userCodeStr, "flag(") || strings.Contains(userCodeStr, "hasFlag(") || strings.Contains(userCodeStr, "colors.") || strings.Contains(userCodeStr, "exitWith(") {
		out.WriteString(stdlib.GetCLIRuntime())
		out.WriteString("\n")
	}

	// Include crypto runtime if crypto functions are used
	if strings.Contains(userCodeStr, "hash(") || strings.Contains(userCodeStr, "encrypt(") || strings.Contains(userCodeStr, "decrypt(") || strings.Contains(userCodeStr, "generateKeys(") || strings.Contains(userCodeStr, "hmac(") || strings.Contains(userCodeStr, "randomBytes(") || strings.Contains(userCodeStr, "uuid(") || strings.Contains(userCodeStr, "hkdf(") || strings.Contains(userCodeStr, "diffieHellman(") || strings.Contains(userCodeStr, "generateX25519Keys(") || strings.Contains(userCodeStr, "argon2(") || strings.Contains(userCodeStr, "argon2Verify(") || strings.Contains(userCodeStr, "bcryptHash(") || strings.Contains(userCodeStr, "bcryptVerify(") || strings.Contains(userCodeStr, "constantTimeEqual(") || strings.Contains(userCodeStr, "aesEncrypt(") || strings.Contains(userCodeStr, "aesDecrypt(") || strings.Contains(userCodeStr, "secureRandomInt(") || strings.Contains(userCodeStr, "secureErase(") || strings.Contains(userCodeStr, "hmacBuffer(") || strings.Contains(userCodeStr, "hkdfBuffer(") || strings.Contains(userCodeStr, "randomBytesBuffer(") {
		out.WriteString(stdlib.GetCryptoRuntime())
		out.WriteString("\n")
	}

	// Include buffer runtime if buffer functions are used
	if strings.Contains(userCodeStr, "toBuffer(") || strings.Contains(userCodeStr, "fromBuffer(") || strings.Contains(userCodeStr, "toBase64(") || strings.Contains(userCodeStr, "fromBase64(") || strings.Contains(userCodeStr, "toHex(") || strings.Contains(userCodeStr, "fromHex(") || strings.Contains(userCodeStr, "concatBuffers(") {
		out.WriteString(stdlib.GetBufferRuntime())
		out.WriteString("\n")
	}

	// Include secure storage runtime if SecureStorage is used (browser mode)
	if g.browser && strings.Contains(userCodeStr, "SecureStorage") {
		out.WriteString(stdlib.GetSecureStorageRuntime())
		out.WriteString("\n")
	}

	// Include WebRTC runtime if peer connection is used
	if strings.Contains(userCodeStr, "createPeer(") || strings.Contains(userCodeStr, "PeerConnection") {
		out.WriteString(stdlib.GetWebRTCRuntime())
		out.WriteString("\n")
	}

	// Include serialization runtime if encode/decode/defineSchema are used
	if strings.Contains(userCodeStr, "defineSchema(") || strings.Contains(userCodeStr, "encode(") || strings.Contains(userCodeStr, "decode(") {
		out.WriteString(stdlib.GetSerializationRuntime())
		out.WriteString("\n")
	}


	// Include Result runtime if error propagation features are used
	if g.needsResultRuntime || strings.Contains(userCodeStr, "Success(") || strings.Contains(userCodeStr, "__propagate(") || strings.Contains(userCodeStr, "__tryResult(") {
		out.WriteString(resultRuntime)
		out.WriteString("\n")
	}

	// Include tagged template runtime functions
	g.injectTagRuntimes(&out)

	// Include decorator runtime if decorators are used
	if g.hasDecorators(program) {
		out.WriteString(decoratorRuntime)
		out.WriteString("\n")
	}

	// Count the preamble lines so source map offsets are correct
	preamble := out.String()
	preambleLines := strings.Count(preamble, "\n")

	// Append command registration code if there are command statements
	commandCode := ""
	if g.hasCommandStatements(program) {
		cmds := g.collectCommands(program)
		commandCode = g.genCommandRegistration(cmds)
	}

	needsAsync := g.programContainsAwait(program) || g.hasAsyncConstructs(program) || g.hasCommandStatements(program)
	if needsAsync {
		g.genLine = preambleLines // (async () => {
		out.WriteString("(async () => {\n")
		preambleLines++ // account for the async wrapper line
		for _, line := range strings.Split(userCodeStr, "\n") {
			if line != "" {
				out.WriteString("  " + line + "\n")
			}
		}
		if commandCode != "" {
			for _, line := range strings.Split(commandCode, "\n") {
				if line != "" {
					out.WriteString("  " + line + "\n")
				}
			}
		}
		out.WriteString("})();\n")
	} else {
		g.genLine = preambleLines
		out.WriteString(userCodeStr)
		if commandCode != "" {
			out.WriteString(commandCode)
		}
	}

	// Offset all source map segments by the preamble lines
	if g.SourceMap != nil {
		for i := range g.SourceMap.segments {
			g.SourceMap.segments[i].genLine += preambleLines
		}
	}

	return out.String()
}

// GenerateWithSourceMap generates JavaScript and a source map JSON string.
func (g *Generator) GenerateWithSourceMap(program *ast.Program, sourceFile, outputFile string) (string, string) {
	g.SourceMap = NewSourceMap(sourceFile, outputFile)

	js := g.Generate(program)

	return js, g.SourceMap.ToJSON()
}

// trackLine counts newlines in output and adds source mappings for statements.
func (g *Generator) addStmtMapping(srcLine int) {
	if g.SourceMap != nil && srcLine > 0 {
		g.SourceMap.AddMapping(g.genLine, 0, srcLine-1, 0)
	}
}


func (g *Generator) genBlock(stmts []ast.Statement) string {
	var out strings.Builder
	for _, stmt := range stmts {
		code := g.genStmt(stmt)
		out.WriteString(code)
		out.WriteString("\n")
		g.genLine += strings.Count(code, "\n") + 1
	}
	return out.String()
}


func (g *Generator) GenerateBody(program *ast.Program) string {
	var out strings.Builder
	for _, stmt := range program.Statements {
		out.WriteString(g.genStmt(stmt))
		out.WriteString("\n")
	}
	return out.String()
}

