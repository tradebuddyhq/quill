package codegen

import (
	"fmt"
	"os"
	"quill/ast"
	"quill/lexer"
	"quill/parser"
	"quill/stdlib"
	"strings"
)

type Generator struct {
	indent               int
	declared             map[string]bool
	browser              bool
	workerMode           bool     // true when generating Cloudflare Worker output (ES modules)
	expoMode             bool     // true when generating React Native / Expo output (JSX)
	SourceMap            *SourceMap
	genLine              int      // current generated line number (0-based)
	needsResultRuntime   bool     // inject Result type runtime helpers
	needsAIRuntime       bool     // inject Anthropic SDK require and client
	needsOpenAIRuntime   bool     // inject OpenAI provider runtime
	needsGeminiRuntime   bool     // inject Gemini provider runtime
	needsOllamaRuntime   bool     // inject Ollama provider runtime
	needsAgentRuntime    bool     // inject Agent class runtime
	needsVectorRuntime   bool     // inject embedding/vector search runtime
	needsDocumentRuntime   bool     // inject document processing runtime
	needsStructuredOutput  bool     // inject structured output parser
	taggedTemplatesUsed  []string // track which tagged template tags are used
	discordClientVar     string   // tracks the variable name for the Discord client
	importedFiles        map[string]bool // tracks imported .quill files to prevent circular imports
}

func New() *Generator {
	return &Generator{
		declared:      make(map[string]bool),
		importedFiles: make(map[string]bool),
	}
}

func NewBrowser() *Generator {
	return &Generator{
		declared:      make(map[string]bool),
		browser:       true,
		importedFiles: make(map[string]bool),
	}
}

func (g *Generator) Generate(program *ast.Program) string {
	// Expo mode: generate React Native / JSX output
	if g.expoMode {
		return g.generateExpo(program)
	}

	// If the program contains a worker handler, use worker mode (ES modules, no Node.js runtime)
	if g.hasWorkerHandler(program) {
		g.workerMode = true
		return g.generateWorker(program)
	}

	var out strings.Builder

	// Generate user code first to check for await
	// Skip CommandStatements - they are handled separately
	var userCode strings.Builder
	for _, stmt := range program.Statements {
		if _, ok := stmt.(*ast.CommandStatement); ok {
			continue
		}
		userCode.WriteString(g.genStmt(stmt))
		userCode.WriteString("\n")
	}
	userCodeStr := userCode.String()

	// Detect AI feature runtimes from generated code
	if strings.Contains(userCodeStr, "embed(") || strings.Contains(userCodeStr, "createVectorStore(") || strings.Contains(userCodeStr, "cosineSimilarity(") {
		g.needsVectorRuntime = true
	}
	if strings.Contains(userCodeStr, "extract(") || strings.Contains(userCodeStr, "chunk(") || strings.Contains(userCodeStr, "splitSentences(") || strings.Contains(userCodeStr, "splitParagraphs(") {
		g.needsDocumentRuntime = true
	}
	if strings.Contains(userCodeStr, "createAgent(") {
		g.needsAgentRuntime = true
	}

	// Check if we need .env loading (env() calls or Discord code or AI code)
	needsEnvLoader := strings.Contains(userCodeStr, "process.env.") || g.hasCommandStatements(program) || strings.Contains(userCodeStr, "Discord") || g.needsAIRuntime || g.needsOpenAIRuntime || g.needsGeminiRuntime || g.needsOllamaRuntime

	out.WriteString("// Generated by Quill\n")

	// Inject .env loader if needed (Node.js only, not browser)
	if needsEnvLoader && !g.browser {
		out.WriteString("try { require('fs').readFileSync('.env', 'utf-8').split('\\n').forEach(line => { const [key, ...rest] = line.split('='); if (key && rest.length) process.env[key.trim()] = rest.join('=').trim(); }); } catch(e) {}\n")
	}

	// Inject Anthropic AI SDK if needed
	if g.needsAIRuntime {
		out.WriteString("const __anthropic = require(\"@anthropic-ai/sdk\");\n")
		out.WriteString("const __ai_client = new __anthropic.default();\n")
	}

	// Inject multi-provider AI runtimes (also needed for structured output parser)
	if g.needsOpenAIRuntime || g.needsGeminiRuntime || g.needsOllamaRuntime || g.needsStructuredOutput {
		out.WriteString(stdlib.GetAIProvidersRuntime())
		out.WriteString("\n")
	}

	// Inject agent runtime
	if g.needsAgentRuntime {
		out.WriteString(stdlib.GetAgentRuntime())
		out.WriteString("\n")
	}

	// Inject vector/embedding runtime
	if g.needsVectorRuntime {
		out.WriteString(stdlib.GetVectorRuntime())
		out.WriteString("\n")
	}

	// Inject document processing runtime
	if g.needsDocumentRuntime {
		out.WriteString(stdlib.GetDocumentRuntime())
		out.WriteString("\n")
	}

	if g.browser {
		out.WriteString(stdlib.BrowserRuntime)
	} else {
		out.WriteString(stdlib.Runtime)
	}
	out.WriteString("\n")

	// Include framework runtime if any component statements are present
	if g.hasComponents(program) {
		out.WriteString(stdlib.FrameworkRuntime)
		out.WriteString("\n")
		// Include utility CSS framework for components
		if g.browser {
			out.WriteString(stdlib.GetUIRuntime())
			out.WriteString("\n")
		}
	}

	// Include template engine runtime if template functions are used
	if strings.Contains(userCodeStr, "tag(") || strings.Contains(userCodeStr, "page(") || strings.Contains(userCodeStr, "escapeHTML(") || strings.Contains(userCodeStr, "layout(") || strings.Contains(userCodeStr, "partial(") || strings.Contains(userCodeStr, "eachItem(") || strings.Contains(userCodeStr, "showIf(") {
		out.WriteString(stdlib.GetTemplateRuntime())
		out.WriteString("\n")
	}

	// Include channel runtime if concurrency features are used
	if g.hasConcurrency(program) {
		out.WriteString(channelRuntime)
		out.WriteString("\n")
	}

	// Include iterator runtime if generator/iterator features are used
	if g.hasGenerators(program) || g.needsIteratorRuntime(userCodeStr) {
		out.WriteString(stdlib.GetIteratorRuntime())
		out.WriteString("\n")
	}

	// Include stdlib runtimes based on usage detection in user code
	if g.usesIdentifier(userCodeStr, "Auth") {
		out.WriteString(stdlib.GetAuthRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "DB") {
		out.WriteString(stdlib.GetOrmRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "Validate") {
		out.WriteString(stdlib.GetValidateRuntime())
		out.WriteString("\n")
	}
	if g.usesIdentifier(userCodeStr, "Log") {
		out.WriteString(stdlib.GetLoggingRuntime())
		out.WriteString("\n")
	}

	// Include Discord bot runtime if discord-related code is detected
	if g.usesIdentifier(userCodeStr, "createBot") {
		out.WriteString(stdlib.DiscordRuntime)
		out.WriteString("\n")
	}

	// Include web server runtime if Express-related code is detected
	if strings.Contains(userCodeStr, "createServer(") || strings.Contains(userCodeStr, "createSecureServer(") {
		out.WriteString(stdlib.WebRuntime)
		out.WriteString("\n")
	}

	// Include CLI runtime if arg/flag functions are used
	if strings.Contains(userCodeStr, "arg(") || strings.Contains(userCodeStr, "args(") || strings.Contains(userCodeStr, "flag(") || strings.Contains(userCodeStr, "hasFlag(") || strings.Contains(userCodeStr, "colors.") || strings.Contains(userCodeStr, "exitWith(") {
		out.WriteString(stdlib.GetCLIRuntime())
		out.WriteString("\n")
	}

	// Include crypto runtime if crypto functions are used
	if strings.Contains(userCodeStr, "hash(") || strings.Contains(userCodeStr, "encrypt(") || strings.Contains(userCodeStr, "decrypt(") || strings.Contains(userCodeStr, "generateKeys(") || strings.Contains(userCodeStr, "hmac(") || strings.Contains(userCodeStr, "randomBytes(") || strings.Contains(userCodeStr, "uuid(") || strings.Contains(userCodeStr, "hkdf(") || strings.Contains(userCodeStr, "diffieHellman(") || strings.Contains(userCodeStr, "generateX25519Keys(") || strings.Contains(userCodeStr, "argon2(") || strings.Contains(userCodeStr, "argon2Verify(") || strings.Contains(userCodeStr, "bcryptHash(") || strings.Contains(userCodeStr, "bcryptVerify(") || strings.Contains(userCodeStr, "constantTimeEqual(") || strings.Contains(userCodeStr, "aesEncrypt(") || strings.Contains(userCodeStr, "aesDecrypt(") || strings.Contains(userCodeStr, "secureRandomInt(") || strings.Contains(userCodeStr, "secureErase(") || strings.Contains(userCodeStr, "hmacBuffer(") || strings.Contains(userCodeStr, "hkdfBuffer(") || strings.Contains(userCodeStr, "randomBytesBuffer(") {
		out.WriteString(stdlib.GetCryptoRuntime())
		out.WriteString("\n")
	}

	// Include buffer runtime if buffer functions are used
	if strings.Contains(userCodeStr, "toBuffer(") || strings.Contains(userCodeStr, "fromBuffer(") || strings.Contains(userCodeStr, "toBase64(") || strings.Contains(userCodeStr, "fromBase64(") || strings.Contains(userCodeStr, "toHex(") || strings.Contains(userCodeStr, "fromHex(") || strings.Contains(userCodeStr, "concatBuffers(") {
		out.WriteString(stdlib.GetBufferRuntime())
		out.WriteString("\n")
	}

	// Include secure storage runtime if SecureStorage is used (browser mode)
	if g.browser && strings.Contains(userCodeStr, "SecureStorage") {
		out.WriteString(stdlib.GetSecureStorageRuntime())
		out.WriteString("\n")
	}

	// Include WebRTC runtime if peer connection is used
	if strings.Contains(userCodeStr, "createPeer(") || strings.Contains(userCodeStr, "PeerConnection") {
		out.WriteString(stdlib.GetWebRTCRuntime())
		out.WriteString("\n")
	}

	// Include serialization runtime if encode/decode/defineSchema are used
	if strings.Contains(userCodeStr, "defineSchema(") || strings.Contains(userCodeStr, "encode(") || strings.Contains(userCodeStr, "decode(") {
		out.WriteString(stdlib.GetSerializationRuntime())
		out.WriteString("\n")
	}


	// Include Result runtime if error propagation features are used
	if g.needsResultRuntime || strings.Contains(userCodeStr, "Success(") || strings.Contains(userCodeStr, "__propagate(") || strings.Contains(userCodeStr, "__tryResult(") {
		out.WriteString(resultRuntime)
		out.WriteString("\n")
	}

	// Include tagged template runtime functions
	g.injectTagRuntimes(&out)

	// Include decorator runtime if decorators are used
	if g.hasDecorators(program) {
		out.WriteString(decoratorRuntime)
		out.WriteString("\n")
	}

	// Count the preamble lines so source map offsets are correct
	preamble := out.String()
	preambleLines := strings.Count(preamble, "\n")

	// Append command registration code if there are command statements
	commandCode := ""
	if g.hasCommandStatements(program) {
		cmds := g.collectCommands(program)
		commandCode = g.genCommandRegistration(cmds)
	}

	needsAsync := g.programContainsAwait(program) || g.hasAsyncConstructs(program) || g.hasCommandStatements(program)
	if needsAsync {
		g.genLine = preambleLines // (async () => {
		out.WriteString("(async () => {\n")
		preambleLines++ // account for the async wrapper line
		for _, line := range strings.Split(userCodeStr, "\n") {
			if line != "" {
				out.WriteString("  " + line + "\n")
			}
		}
		if commandCode != "" {
			for _, line := range strings.Split(commandCode, "\n") {
				if line != "" {
					out.WriteString("  " + line + "\n")
				}
			}
		}
		out.WriteString("})();\n")
	} else {
		g.genLine = preambleLines
		out.WriteString(userCodeStr)
		if commandCode != "" {
			out.WriteString(commandCode)
		}
	}

	// Offset all source map segments by the preamble lines
	if g.SourceMap != nil {
		for i := range g.SourceMap.segments {
			g.SourceMap.segments[i].genLine += preambleLines
		}
	}

	return out.String()
}

// GenerateWithSourceMap generates JavaScript and a source map JSON string.
func (g *Generator) GenerateWithSourceMap(program *ast.Program, sourceFile, outputFile string) (string, string) {
	g.SourceMap = NewSourceMap(sourceFile, outputFile)

	js := g.Generate(program)

	return js, g.SourceMap.ToJSON()
}

// trackLine counts newlines in output and adds source mappings for statements.
func (g *Generator) addStmtMapping(srcLine int) {
	if g.SourceMap != nil && srcLine > 0 {
		g.SourceMap.AddMapping(g.genLine, 0, srcLine-1, 0)
	}
}

func (g *Generator) genStmt(stmt ast.Statement) string {
	prefix := strings.Repeat("  ", g.indent)

	switch s := stmt.(type) {
	case *ast.AssignStatement:
		g.addStmtMapping(s.Line)
		// Detect Discord.bot() or new Discord.Client() assignment to track the client variable name
		if call, ok := s.Value.(*ast.CallExpr); ok {
			if dot, ok := call.Function.(*ast.DotExpr); ok {
				if ident, ok := dot.Object.(*ast.Identifier); ok && ident.Name == "Discord" && dot.Field == "bot" {
					g.discordClientVar = s.Name
				}
			}
		}
		if newExpr, ok := s.Value.(*ast.NewExpr); ok {
			if newExpr.ClassName == "Discord.Client" {
				g.discordClientVar = s.Name
			}
		}
		value := g.genExpr(s.Value)
		if g.declared[s.Name] {
			return fmt.Sprintf("%s%s = %s;", prefix, s.Name, value)
		}
		g.declared[s.Name] = true
		return fmt.Sprintf("%slet %s = %s;", prefix, s.Name, value)

	case *ast.SayStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%sconsole.log(%s);", prefix, g.genExpr(s.Value))

	case *ast.IfStatement:
		g.addStmtMapping(s.Line)
		return g.genIf(s, prefix)

	case *ast.ForEachStatement:
		g.addStmtMapping(s.Line)
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		varPart := s.Variable
		if s.DestructurePattern != nil {
			varPart = g.genPattern(s.DestructurePattern)
		}
		awaitPart := ""
		if s.IsAsync {
			awaitPart = "await "
		}
		return fmt.Sprintf("%sfor %s(const %s of %s) {\n%s%s}", prefix, awaitPart, varPart, g.genExpr(s.Iterable), body, prefix)

	case *ast.WhileStatement:
		g.addStmtMapping(s.Line)
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		return fmt.Sprintf("%swhile (%s) {\n%s%s}", prefix, g.genExpr(s.Condition), body, prefix)

	case *ast.FuncDefinition:
		g.addStmtMapping(s.Line)
		// Save the current declared map and create a new scope for the function
		// Copy the outer scope so the function knows about already-declared variables
		outerDeclared := g.declared
		g.declared = make(map[string]bool)
		for k, v := range outerDeclared {
			g.declared[k] = v
		}
		// Mark function params as declared in the new scope
		for _, param := range s.Params {
			g.declared[param] = true
		}
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		// Restore the outer scope
		g.declared = outerDeclared
		params := strings.Join(s.Params, ", ")
		if g.bodyContainsYield(s.Body) {
			return fmt.Sprintf("%sfunction* %s(%s) {\n%s%s}", prefix, s.Name, params, body, prefix)
		}
		if g.bodyContainsAwait(s.Body) {
			return fmt.Sprintf("%sasync function %s(%s) {\n%s%s}", prefix, s.Name, params, body, prefix)
		}
		return fmt.Sprintf("%sfunction %s(%s) {\n%s%s}", prefix, s.Name, params, body, prefix)

	case *ast.YieldStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%syield %s;", prefix, g.genExpr(s.Value))

	case *ast.LoopStatement:
		g.addStmtMapping(s.Line)
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		return fmt.Sprintf("%swhile (true) {\n%s%s}", prefix, body, prefix)

	case *ast.ReturnStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%sreturn %s;", prefix, g.genExpr(s.Value))

	case *ast.ExprStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%s%s;", prefix, g.genExpr(s.Expr))

	case *ast.DotAssignStatement:
		return fmt.Sprintf("%s%s.%s = %s;", prefix, s.Object, s.Field, g.genExpr(s.Value))

	case *ast.IndexAssignStatement:
		return fmt.Sprintf("%s%s[%s] = %s;", prefix, g.genExpr(s.Object), g.genExpr(s.Index), g.genExpr(s.Value))

	case *ast.DescribeStatement:
		var dout strings.Builder
		if s.Extends != "" {
			dout.WriteString(fmt.Sprintf("%sclass %s extends %s {\n", prefix, s.Name, s.Extends))
		} else {
			dout.WriteString(fmt.Sprintf("%sclass %s {\n", prefix, s.Name))
		}
		g.indent++
		innerPrefix := strings.Repeat("  ", g.indent)

		// Emit private field declarations (JS requires # fields to be declared)
		for i, prop := range s.Properties {
			vis := ""
			if i < len(s.PropertyVisibilities) {
				vis = s.PropertyVisibilities[i]
			}
			if vis == "private" {
				dout.WriteString(fmt.Sprintf("%s#%s;\n", innerPrefix, prop.Name))
			}
		}

		// Constructor
		dout.WriteString(fmt.Sprintf("%sconstructor() {\n", innerPrefix))
		if s.Extends != "" {
			g.indent++
			superPrefix := strings.Repeat("  ", g.indent)
			dout.WriteString(fmt.Sprintf("%ssuper();\n", superPrefix))
			g.indent--
		}
		g.indent++
		propPrefix := strings.Repeat("  ", g.indent)
		for i, prop := range s.Properties {
			vis := ""
			if i < len(s.PropertyVisibilities) {
				vis = s.PropertyVisibilities[i]
			}
			if vis == "private" {
				dout.WriteString(fmt.Sprintf("%sthis.#%s = %s;\n", propPrefix, prop.Name, g.genExpr(prop.Value)))
			} else {
				dout.WriteString(fmt.Sprintf("%sthis.%s = %s;\n", propPrefix, prop.Name, g.genExpr(prop.Value)))
			}
		}
		g.indent--
		dout.WriteString(fmt.Sprintf("%s}\n", innerPrefix))
		// Methods
		for i, method := range s.Methods {
			vis := ""
			if i < len(s.MethodVisibilities) {
				vis = s.MethodVisibilities[i]
			}
			params := strings.Join(method.Params, ", ")
			if vis == "private" {
				dout.WriteString(fmt.Sprintf("%s#%s(%s) {\n", innerPrefix, method.Name, params))
			} else {
				dout.WriteString(fmt.Sprintf("%s%s(%s) {\n", innerPrefix, method.Name, params))
			}
			g.indent++
			for _, stmt := range method.Body {
				dout.WriteString(g.genStmt(stmt))
				dout.WriteString("\n")
			}
			g.indent--
			dout.WriteString(fmt.Sprintf("%s}\n", innerPrefix))
		}
		g.indent--
		dout.WriteString(fmt.Sprintf("%s}", prefix))
		return dout.String()

	case *ast.UseStatement:
		if strings.HasSuffix(s.Path, ".quill") {
			// Circular import protection
			if g.importedFiles[s.Path] {
				return fmt.Sprintf("%s// skipped circular import of %q", prefix, s.Path)
			}
			g.importedFiles[s.Path] = true
			imported, err := os.ReadFile(s.Path)
			if err != nil {
				return fmt.Sprintf("%s// Error: could not import %q", prefix, s.Path)
			}
			l := lexer.New(string(imported))
			tokens, err := l.Tokenize()
			if err != nil {
				return fmt.Sprintf("%s// Error: could not tokenize %q: %s", prefix, s.Path, err)
			}
			pr := parser.New(tokens)
			prog, err := pr.Parse()
			if err != nil || prog == nil {
				return fmt.Sprintf("%s// Error: could not parse %q", prefix, s.Path)
			}
			var importGen *Generator
			if g.browser {
				importGen = NewBrowser()
			} else {
				importGen = New()
			}
			importGen.indent = g.indent
			// Share the importedFiles map to prevent circular imports across transitive imports
			importGen.importedFiles = g.importedFiles
			code := importGen.GenerateBody(prog)
			return fmt.Sprintf("%s// imported from %q\n%s", prefix, s.Path, code)
		}
		// NPM package - use import for worker mode, require for Node
		varName := strings.ReplaceAll(s.Path, "-", "_")
		varName = strings.ReplaceAll(varName, "/", "_")
		varName = strings.ReplaceAll(varName, "@", "")
		varName = strings.ReplaceAll(varName, ".", "_")
		if s.Alias != "" {
			varName = s.Alias
		}
		if g.workerMode {
			return fmt.Sprintf("%simport %s from \"%s\";", prefix, varName, s.Path)
		}
		return fmt.Sprintf("%sconst %s = require(\"%s\");", prefix, varName, s.Path)

	case *ast.TestBlock:
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		return fmt.Sprintf(`%s(() => {
%s  try {
%s%s    __test_passed++;
%s    console.log("  \u2713 %s");
%s  } catch(e) {
%s    __test_failed++;
%s    console.log("  \u2717 %s:", e.message);
%s  }
%s})();`, prefix, prefix, prefix, body, prefix, s.Name, prefix, prefix, prefix, s.Name, prefix, prefix)

	case *ast.MockStatement:
		var mout strings.Builder
		mout.WriteString(fmt.Sprintf("%sconst __mock_%s_calls = [];\n", prefix, s.FuncName))
		mout.WriteString(fmt.Sprintf("%sconst __original_%s = typeof %s !== 'undefined' ? %s : undefined;\n", prefix, s.FuncName, s.FuncName, s.FuncName))
		params := strings.Join(s.Params, ", ")
		mout.WriteString(fmt.Sprintf("%svar %s = function(%s) {\n", prefix, s.FuncName, params))
		args := s.Params
		argsArray := "[]"
		if len(args) > 0 {
			argsArray = "[" + strings.Join(args, ", ") + "]"
		}
		mout.WriteString(fmt.Sprintf("%s  __mock_%s_calls.push({args: %s});\n", prefix, s.FuncName, argsArray))
		g.indent += 2
		for _, stmt := range s.Body {
			mout.WriteString(g.genStmt(stmt))
			mout.WriteString("\n")
		}
		g.indent -= 2
		mout.WriteString(fmt.Sprintf("%s};\n", prefix))
		return mout.String()

	case *ast.ExpectStatement:
		if ma, ok := s.Expr.(*ast.MockAssertionExpr); ok {
			return fmt.Sprintf(`%sif (__mock_%s_calls.length !== %d) throw new Error("Expected %s to be called %d time(s), was called " + __mock_%s_calls.length + " time(s)");`,
				prefix, ma.FuncName, ma.Count, ma.FuncName, ma.Count, ma.FuncName)
		}
		exprStr := g.genExpr(s.Expr)
		return fmt.Sprintf(`%sif (!(%s)) throw new Error("Expected %s to be true");`, prefix, exprStr, escapeJS(exprStr))

	case *ast.TryCatchStatement:
		g.indent++
		tryBody := g.genBlock(s.TryBody)
		g.indent--
		g.indent++
		catchBody := g.genBlock(s.CatchBody)
		g.indent--
		errorVar := s.ErrorVar
		if errorVar == "" {
			errorVar = "error"
		}
		return fmt.Sprintf("%stry {\n%s%s} catch (%s) {\n%s%s}", prefix, tryBody, prefix, errorVar, catchBody, prefix)

	case *ast.DeleteStatement:
		return fmt.Sprintf("%sdelete %s;", prefix, g.genExpr(s.Target))

	case *ast.BreakStatement:
		return fmt.Sprintf("%sbreak;", prefix)

	case *ast.ContinueStatement:
		return fmt.Sprintf("%scontinue;", prefix)

	case *ast.MatchStatement:
		return g.genMatch(s, prefix)

	case *ast.DefineStatement:
		return g.genDefine(s, prefix)

	case *ast.TypedAssignStatement:
		value := g.genExpr(s.Value)
		if g.declared[s.Name] {
			return fmt.Sprintf("%s%s = %s;", prefix, s.Name, value)
		}
		g.declared[s.Name] = true
		return fmt.Sprintf("%slet %s = %s;", prefix, s.Name, value)

	case *ast.FromUseStatement:
		names := strings.Join(s.Names, ", ")
		if strings.HasSuffix(s.Path, ".quill") {
			// Circular import protection
			if g.importedFiles[s.Path] {
				return fmt.Sprintf("%s// skipped circular import of %q", prefix, s.Path)
			}
			g.importedFiles[s.Path] = true
			imported, err := os.ReadFile(s.Path)
			if err != nil {
				return fmt.Sprintf("%s// Error: could not import %q", prefix, s.Path)
			}
			l := lexer.New(string(imported))
			tokens, err := l.Tokenize()
			if err != nil {
				return fmt.Sprintf("%s// Error: could not tokenize %q: %s", prefix, s.Path, err)
			}
			pr := parser.New(tokens)
			prog, err := pr.Parse()
			if err != nil || prog == nil {
				return fmt.Sprintf("%s// Error: could not parse %q", prefix, s.Path)
			}
			var importGen *Generator
			if g.browser {
				importGen = NewBrowser()
			} else {
				importGen = New()
			}
			importGen.indent = g.indent
			importGen.importedFiles = g.importedFiles
			code := importGen.GenerateBody(prog)
			return fmt.Sprintf("%s// imported from %q\n%s", prefix, s.Path, code)
		}
		if g.workerMode {
			return fmt.Sprintf("%simport { %s } from \"%s\";", prefix, names, s.Path)
		}
		return fmt.Sprintf("%sconst { %s } = require(\"%s\");", prefix, names, s.Path)

	case *ast.ComponentStatement:
		return g.genComponent(s, prefix)

	case *ast.MountStatement:
		return g.genMount(s, prefix)

	case *ast.CancelStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%s__abort_%s.abort();", prefix, s.Target)

	case *ast.SpawnStatement:
		g.addStmtMapping(s.Line)
		return g.genSpawn(s, prefix)

	case *ast.ParallelBlock:
		g.addStmtMapping(s.Line)
		return g.genParallel(s, prefix)

	case *ast.RaceBlock:
		g.addStmtMapping(s.Line)
		return g.genRaceBlock(s, prefix)

	case *ast.ChannelStatement:
		g.addStmtMapping(s.Line)
		return g.genChannelStmt(s, prefix)

	case *ast.SendStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%sawait %s.send(%s);", prefix, s.Channel, g.genExpr(s.Value))

	case *ast.SelectStatement:
		g.addStmtMapping(s.Line)
		return g.genSelectStmt(s, prefix)

	case *ast.TraitDeclaration:
		return g.genTrait(s, prefix)

	case *ast.DestructureStatement:
		return g.genDestructure(s, prefix)

	case *ast.RespondStatement:
		g.addStmtMapping(s.Line)
		if g.workerMode {
			return g.genWorkerRespond(s, prefix)
		}
		statusCode := s.StatusCode
		if statusCode == 0 {
			statusCode = 200
		}
		return fmt.Sprintf("%sres.writeHead(%d, {'Content-Type': 'application/json'}); res.end(JSON.stringify(%s));", prefix, statusCode, g.genExpr(s.Value))

	case *ast.EveryStatement:
		g.addStmtMapping(s.Line)
		ms := s.Interval
		switch s.Unit {
		case "seconds":
			ms = s.Interval * 1000
		case "minutes":
			ms = s.Interval * 60000
		case "hours":
			ms = s.Interval * 3600000
		}
		g.indent++
		body := g.genBlock(s.Body)
		g.indent--
		return fmt.Sprintf("%ssetInterval(() => {\n%s%s}, %d);", prefix, body, prefix, ms)

	case *ast.AgentStatement:
		g.addStmtMapping(s.Line)
		g.needsAgentRuntime = true
		// Also need provider runtime for the agent
		switch s.Provider {
		case "openai":
			g.needsOpenAIRuntime = true
		case "gemini":
			g.needsGeminiRuntime = true
		case "ollama":
			g.needsOllamaRuntime = true
		default:
			g.needsAIRuntime = true
		}
		var out strings.Builder
		agentVar := fmt.Sprintf("__agent_%s", strings.ReplaceAll(s.Name, " ", "_"))
		providerStr := s.Provider
		if providerStr == "" {
			providerStr = "claude"
		}
		out.WriteString(fmt.Sprintf("%sconst %s = createAgent(%q, { provider: %q", prefix, agentVar, s.Name, providerStr))
		if m, ok := s.Options["model"]; ok {
			out.WriteString(fmt.Sprintf(", model: %s", g.genExpr(m)))
		}
		if sys, ok := s.Options["system"]; ok {
			out.WriteString(fmt.Sprintf(", system: %s", g.genExpr(sys)))
		}
		out.WriteString(" });\n")
		// Register tools
		for _, tool := range s.Tools {
			out.WriteString(fmt.Sprintf("%s%s.addTool(%q, %q, %s);\n", prefix, agentVar, tool, tool+" tool", tool))
		}
		// Generate body (agent.run call and handling)
		g.indent++
		out.WriteString(g.genBlock(s.Body))
		g.indent--
		return out.String()

	case *ast.NavigateStatement:
		g.addStmtMapping(s.Line)
		if s.Params != nil {
			return fmt.Sprintf("%snavigation.navigate(\"%s\", %s);", prefix, s.Screen, g.genExpr(s.Params))
		}
		return fmt.Sprintf("%snavigation.navigate(\"%s\");", prefix, s.Screen)

	case *ast.NavigationBlock:
		return prefix + "/* navigation block — use 'quill build --expo' for Expo mode */"

	case *ast.WorkerHandler:
		g.addStmtMapping(s.Line)
		return g.genWorkerHandler(s, prefix)

	case *ast.ServerBlockStatement:
		return prefix + "/* server block — use 'quill run' for full-stack mode */"

	case *ast.DatabaseBlockStatement:
		return prefix + "/* database block — use 'quill run' for full-stack mode */"

	case *ast.AuthBlockStatement:
		return prefix + "/* auth block — use 'quill run' for full-stack mode */"

	case *ast.TypeAliasStatement:
		return g.genTypeAlias(s, prefix)

	case *ast.DecoratedFuncDefinition:
		return g.genDecoratedFunc(s, prefix)

	case *ast.DecoratedRouteDefinition:
		return g.genDecoratedRoute(s, prefix)

	case *ast.BroadcastStatement:
		return fmt.Sprintf("%sfor (const __c of __ws_clients) { if (__c !== client && __c.readyState === 1) { __c.send(%s); } }", prefix, g.genExpr(s.Value))

	case *ast.WebSocketBlock:
		return g.genWebSocket(s, prefix)

	case *ast.CommandStatement:
		// Command statements are collected and handled in Generate()
		// This should not normally be reached directly
		return prefix + "/* command statement — handled at top level */"

	case *ast.ReplyStatement:
		g.addStmtMapping(s.Line)
		return fmt.Sprintf("%sinteraction.reply(%s);", prefix, g.genExpr(s.Value))

	case *ast.StreamStatement:
		g.addStmtMapping(s.Line)
		var out strings.Builder
		prompt := g.genExpr(s.Prompt)

		// Build options for non-claude providers
		optParts := []string{}
		if m, ok := s.Options["model"]; ok {
			optParts = append(optParts, fmt.Sprintf("model: %s", g.genExpr(m)))
		}
		if mt, ok := s.Options["max_tokens"]; ok {
			optParts = append(optParts, fmt.Sprintf("max_tokens: %s", g.genExpr(mt)))
		}
		if sys, ok := s.Options["system"]; ok {
			optParts = append(optParts, fmt.Sprintf("system: %s", g.genExpr(sys)))
		}
		if temp, ok := s.Options["temperature"]; ok {
			optParts = append(optParts, fmt.Sprintf("temperature: %s", g.genExpr(temp)))
		}
		opts := "{" + strings.Join(optParts, ", ") + "}"

		switch s.Provider {
		case "openai", "gemini", "ollama":
			switch s.Provider {
			case "openai":
				g.needsOpenAIRuntime = true
			case "gemini":
				g.needsGeminiRuntime = true
			case "ollama":
				g.needsOllamaRuntime = true
			}
			streamFn := fmt.Sprintf("__stream_%s", s.Provider)
			out.WriteString(fmt.Sprintf("%sfor await (const %s of %s(%s, %s)) {\n", prefix, s.ChunkVar, streamFn, prompt, opts))
			g.indent++
			out.WriteString(g.genBlock(s.Body))
			g.indent--
			out.WriteString(fmt.Sprintf("%s}", prefix))
		default: // claude
			g.needsAIRuntime = true
			model := `"claude-sonnet-4-20250514"`
			maxTokens := "1024"
			if m, ok := s.Options["model"]; ok {
				model = g.genExpr(m)
			}
			if mt, ok := s.Options["max_tokens"]; ok {
				maxTokens = g.genExpr(mt)
			}
			out.WriteString(fmt.Sprintf("%sconst __stream = await __ai_client.messages.stream({\n", prefix))
			out.WriteString(fmt.Sprintf("%s  model: %s,\n", prefix, model))
			out.WriteString(fmt.Sprintf("%s  max_tokens: %s,\n", prefix, maxTokens))
			if sys, ok := s.Options["system"]; ok {
				out.WriteString(fmt.Sprintf("%s  system: %s,\n", prefix, g.genExpr(sys)))
			}
			if temp, ok := s.Options["temperature"]; ok {
				out.WriteString(fmt.Sprintf("%s  temperature: %s,\n", prefix, g.genExpr(temp)))
			}
			out.WriteString(fmt.Sprintf("%s  messages: [{ role: \"user\", content: %s }]\n", prefix, g.genExpr(s.Prompt)))
			out.WriteString(fmt.Sprintf("%s});\n", prefix))
			out.WriteString(fmt.Sprintf("%sfor await (const __event of __stream) {\n", prefix))
			out.WriteString(fmt.Sprintf("%s  if (__event.type === \"content_block_delta\") {\n", prefix))
			out.WriteString(fmt.Sprintf("%s    const %s = __event.delta.text;\n", prefix, s.ChunkVar))
			g.indent += 2
			out.WriteString(g.genBlock(s.Body))
			g.indent -= 2
			out.WriteString(fmt.Sprintf("%s  }\n", prefix))
			out.WriteString(fmt.Sprintf("%s}", prefix))
		}
		return out.String()

	case *ast.OnStatement:
		var out strings.Builder
		params := strings.Join(s.Params, ", ")
		if s.Method != "" {
			// Route handler: app.get("/path", (req, res) => { ... })
			out.WriteString(fmt.Sprintf("%s%s.%s(\"%s\", ", prefix, g.genExpr(s.Object), s.Method, s.Path))
		} else {
			// Event handler: obj.on("event", ...)
			out.WriteString(fmt.Sprintf("%s%s.on(\"%s\", ", prefix, g.genExpr(s.Object), s.Event))
		}
		if len(s.Params) > 0 {
			out.WriteString(fmt.Sprintf("(%s) => {\n", params))
		} else {
			out.WriteString("() => {\n")
		}
		g.indent++
		out.WriteString(g.genBlock(s.Body))
		g.indent--
		out.WriteString(fmt.Sprintf("%s});", prefix))
		return out.String()

	default:
		return prefix + "/* unknown statement */"
	}
}

func (g *Generator) genIf(s *ast.IfStatement, prefix string) string {
	var out strings.Builder

	g.indent++
	body := g.genBlock(s.Body)
	g.indent--

	out.WriteString(fmt.Sprintf("%sif (%s) {\n%s%s}", prefix, g.genExpr(s.Condition), body, prefix))

	for _, elif := range s.ElseIfs {
		g.indent++
		elifBody := g.genBlock(elif.Body)
		g.indent--
		out.WriteString(fmt.Sprintf(" else if (%s) {\n%s%s}", g.genExpr(elif.Condition), elifBody, prefix))
	}

	if len(s.Else) > 0 {
		g.indent++
		elseBody := g.genBlock(s.Else)
		g.indent--
		out.WriteString(fmt.Sprintf(" else {\n%s%s}", elseBody, prefix))
	}

	return out.String()
}

func (g *Generator) genMatch(s *ast.MatchStatement, prefix string) string {
	var out strings.Builder
	matchVar := "__match_val"
	out.WriteString(fmt.Sprintf("%s{\n", prefix))
	g.indent++
	innerPrefix := strings.Repeat("  ", g.indent)
	out.WriteString(fmt.Sprintf("%sconst %s = %s;\n", innerPrefix, matchVar, g.genExpr(s.Value)))

	for i, c := range s.Cases {
		// Build body with possible binding prefix
		var bodyStr string
		if c.TypePattern != "" && c.Binding != "" {
			// Inject binding variable at the top of the body
			g.indent++
			bindPrefix := strings.Repeat("  ", g.indent)
			bodyStr = fmt.Sprintf("%slet %s = %s;\n", bindPrefix, c.Binding, matchVar)
			bodyStr += g.genBlock(c.Body)
			g.indent--
		} else {
			g.indent++
			bodyStr = g.genBlock(c.Body)
			g.indent--
		}

		if c.TypePattern != "" {
			// Type-based pattern matching
			condition := g.genTypeCondition(c.TypePattern, matchVar)
			if c.Guard != nil {
				condition = fmt.Sprintf("(%s && %s)", condition, g.genExpr(c.Guard))
			}
			if i == 0 {
				out.WriteString(fmt.Sprintf("%sif %s {\n%s%s}", innerPrefix, condition, bodyStr, innerPrefix))
			} else {
				out.WriteString(fmt.Sprintf(" else if %s {\n%s%s}", condition, bodyStr, innerPrefix))
			}
		} else if objPat, ok := c.Pattern.(*ast.ObjectMatchPattern); ok {
			// Object destructuring pattern
			var conditions []string
			var bindings []string
			for _, f := range objPat.Fields {
				if f.Value != nil {
					conditions = append(conditions, fmt.Sprintf("%s.%s === %s", matchVar, f.Key, g.genExpr(f.Value)))
				} else {
					// Just bind the field
					bindings = append(bindings, f.Key)
				}
			}
			condition := "true"
			if len(conditions) > 0 {
				condition = strings.Join(conditions, " && ")
			}
			if c.Guard != nil {
				condition = fmt.Sprintf("(%s && %s)", condition, g.genExpr(c.Guard))
			}

			// Inject bindings at the top of the body
			g.indent++
			bindPrefix := strings.Repeat("  ", g.indent)
			var bindStr string
			for _, b := range bindings {
				bindStr += fmt.Sprintf("%sconst %s = %s.%s;\n", bindPrefix, b, matchVar, b)
			}
			bodyStr = bindStr + bodyStr
			g.indent--

			if i == 0 {
				out.WriteString(fmt.Sprintf("%sif (%s) {\n%s%s}", innerPrefix, condition, bodyStr, innerPrefix))
			} else {
				out.WriteString(fmt.Sprintf(" else if (%s) {\n%s%s}", condition, bodyStr, innerPrefix))
			}
		} else if c.Pattern == nil {
			// otherwise case
			if i == 0 {
				out.WriteString(fmt.Sprintf("%sif (true) {\n%s%s}", innerPrefix, bodyStr, innerPrefix))
			} else {
				out.WriteString(fmt.Sprintf(" else {\n%s%s}", bodyStr, innerPrefix))
			}
		} else {
			// Value-based pattern
			condition := fmt.Sprintf("(%s === %s)", matchVar, g.genExpr(c.Pattern))
			if c.Guard != nil {
				condition = fmt.Sprintf("(%s === %s && %s)", matchVar, g.genExpr(c.Pattern), g.genExpr(c.Guard))
			}

			if i == 0 {
				out.WriteString(fmt.Sprintf("%sif %s {\n%s%s}", innerPrefix, condition, bodyStr, innerPrefix))
			} else {
				out.WriteString(fmt.Sprintf(" else if %s {\n%s%s}", condition, bodyStr, innerPrefix))
			}
		}
	}
	out.WriteString("\n")
	g.indent--
	out.WriteString(fmt.Sprintf("%s}", prefix))
	return out.String()
}

func (g *Generator) genDefine(s *ast.DefineStatement, prefix string) string {
	var out strings.Builder

	// Check if any variant has an associated value (enum with values + methods)
	hasValues := false
	for _, v := range s.Variants {
		if v.Value != nil {
			hasValues = true
			break
		}
	}

	if hasValues && len(s.Methods) > 0 {
		// Generate class-based enum with methods
		enumClassName := s.Name + "Enum"
		out.WriteString(fmt.Sprintf("%sconst %s = (() => {\n", prefix, s.Name))
		g.indent++
		ip := strings.Repeat("  ", g.indent)
		out.WriteString(fmt.Sprintf("%sclass %s {\n", ip, enumClassName))
		g.indent++
		ip2 := strings.Repeat("  ", g.indent)
		out.WriteString(fmt.Sprintf("%sconstructor(name, value) { this.name = name; this.value = value; Object.freeze(this); }\n", ip2))
		for _, method := range s.Methods {
			params := strings.Join(method.Params, ", ")
			out.WriteString(fmt.Sprintf("%s%s(%s) {\n", ip2, method.Name, params))
			g.indent++
			for _, stmt := range method.Body {
				out.WriteString(g.genStmt(stmt))
				out.WriteString("\n")
			}
			g.indent--
			out.WriteString(fmt.Sprintf("%s}\n", ip2))
		}
		g.indent--
		out.WriteString(fmt.Sprintf("%s}\n", ip))
		out.WriteString(fmt.Sprintf("%sreturn Object.freeze({\n", ip))
		g.indent++
		ip3 := strings.Repeat("  ", g.indent)
		for i, variant := range s.Variants {
			valStr := "undefined"
			if variant.Value != nil {
				valStr = g.genExpr(variant.Value)
			}
			out.WriteString(fmt.Sprintf("%s%s: new %s(\"%s\", %s)", ip3, variant.Name, enumClassName, variant.Name, valStr))
			if i < len(s.Variants)-1 {
				out.WriteString(",")
			}
			out.WriteString("\n")
		}
		g.indent--
		out.WriteString(fmt.Sprintf("%s});\n", ip))
		g.indent--
		out.WriteString(fmt.Sprintf("%s})();\n", prefix))
	} else if hasValues {
		// Enum with values but no methods
		out.WriteString(fmt.Sprintf("%sconst %s = Object.freeze({\n", prefix, s.Name))
		g.indent++
		innerPrefix := strings.Repeat("  ", g.indent)
		for i, variant := range s.Variants {
			valStr := "undefined"
			if variant.Value != nil {
				valStr = g.genExpr(variant.Value)
			}
			out.WriteString(fmt.Sprintf("%s%s: Object.freeze({ type: \"%s\", variant: \"%s\", name: \"%s\", value: %s, toString() { return \"%s.%s\"; } })",
				innerPrefix, variant.Name, s.Name, variant.Name, variant.Name, valStr, s.Name, variant.Name))
			if i < len(s.Variants)-1 {
				out.WriteString(",")
			}
			out.WriteString("\n")
		}
		g.indent--
		out.WriteString(fmt.Sprintf("%s});\n", prefix))
	} else {
		// Original behavior: frozen object with variant constructors
		out.WriteString(fmt.Sprintf("%sconst %s = Object.freeze({\n", prefix, s.Name))
		g.indent++
		innerPrefix := strings.Repeat("  ", g.indent)

		for i, variant := range s.Variants {
			if len(variant.Fields) == 0 {
				out.WriteString(fmt.Sprintf("%s%s: Object.freeze({ type: \"%s\", variant: \"%s\", toString() { return \"%s.%s\"; } })", innerPrefix, variant.Name, s.Name, variant.Name, s.Name, variant.Name))
			} else {
				params := strings.Join(variant.Fields, ", ")
				fieldAssignments := make([]string, len(variant.Fields))
				for j, field := range variant.Fields {
					fieldAssignments[j] = fmt.Sprintf("%s: %s", field, field)
				}
				out.WriteString(fmt.Sprintf("%s%s: (%s) => Object.freeze({ type: \"%s\", variant: \"%s\", %s, toString() { return \"%s.%s(\" + [%s].join(\", \") + \")\"; } })",
					innerPrefix, variant.Name, params, s.Name, variant.Name, strings.Join(fieldAssignments, ", "), s.Name, variant.Name, params))
			}
			if i < len(s.Variants)-1 {
				out.WriteString(",")
			}
			out.WriteString("\n")
		}

		g.indent--
		out.WriteString(fmt.Sprintf("%s});\n", prefix))
	}

	// Add helper: is<Variant> functions
	for _, variant := range s.Variants {
		out.WriteString(fmt.Sprintf("%sconst is%s = (v) => v && v.variant === \"%s\";\n", prefix, variant.Name, variant.Name))
	}

	return out.String()
}

func (g *Generator) genBlock(stmts []ast.Statement) string {
	var out strings.Builder
	for _, stmt := range stmts {
		code := g.genStmt(stmt)
		out.WriteString(code)
		out.WriteString("\n")
		g.genLine += strings.Count(code, "\n") + 1
	}
	return out.String()
}

func (g *Generator) genExpr(expr ast.Expression) string {
	switch e := expr.(type) {
	case *ast.StringLiteral:
		if hasInterpolation(e.Value) {
			// Convert {var} to ${var} for JS template literals
			converted := e.Value
			converted = strings.ReplaceAll(converted, "`", "\\`")
			// Convert my.field to this.field only inside interpolation braces
			converted = replaceMyInInterpolations(converted)
			converted = convertInterpolation(converted)
			return "`" + converted + "`"
		}
		// The lexer preserves escape sequences as-is (e.g. \n stays as backslash + n),
		// which are already valid JS escape sequences. Only escape unescaped double quotes.
		escaped := strings.ReplaceAll(e.Value, "\n", "\\n")  // actual newline bytes (shouldn't occur, but safety)
		escaped = strings.ReplaceAll(escaped, "\r", "\\r")
		escaped = strings.ReplaceAll(escaped, "\t", "\\t")
		escaped = strings.ReplaceAll(escaped, "\x00", "\\0")
		return `"` + escaped + `"`

	case *ast.NumberLiteral:
		if e.Value == float64(int64(e.Value)) {
			return fmt.Sprintf("%d", int64(e.Value))
		}
		return fmt.Sprintf("%g", e.Value)

	case *ast.BoolLiteral:
		if e.Value {
			return "true"
		}
		return "false"

	case *ast.Identifier:
		return e.Name

	case *ast.ListLiteral:
		elems := make([]string, len(e.Elements))
		for i, el := range e.Elements {
			elems[i] = g.genExpr(el)
		}
		return "[" + strings.Join(elems, ", ") + "]"

	case *ast.BinaryExpr:
		if e.Operator == "^" {
			return fmt.Sprintf("(%s ** %s)", g.genExpr(e.Left), g.genExpr(e.Right))
		}
		return fmt.Sprintf("(%s %s %s)", g.genExpr(e.Left), e.Operator, g.genExpr(e.Right))

	case *ast.TernaryExpression:
		return fmt.Sprintf("(%s ? %s : %s)", g.genExpr(e.Condition), g.genExpr(e.Then), g.genExpr(e.Else))

	case *ast.ComparisonExpr:
		if e.Operator == "contains" {
			return fmt.Sprintf("__contains(%s, %s)", g.genExpr(e.Left), g.genExpr(e.Right))
		}
		op := e.Operator
		// For null comparisons, use == / != to catch both null and undefined
		_, leftIsNothing := e.Left.(*ast.NothingLiteral)
		_, rightIsNothing := e.Right.(*ast.NothingLiteral)
		isNullComparison := leftIsNothing || rightIsNothing
		if op == "==" && !isNullComparison {
			op = "==="
		}
		if op == "!=" && !isNullComparison {
			op = "!=="
		}
		return fmt.Sprintf("(%s %s %s)", g.genExpr(e.Left), op, g.genExpr(e.Right))

	case *ast.LogicalExpr:
		op := "&&"
		if e.Operator == "or" {
			op = "||"
		}
		return fmt.Sprintf("(%s %s %s)", g.genExpr(e.Left), op, g.genExpr(e.Right))

	case *ast.NotExpr:
		return fmt.Sprintf("(!%s)", g.genExpr(e.Operand))

	case *ast.UnaryMinusExpr:
		return fmt.Sprintf("(-%s)", g.genExpr(e.Operand))

	case *ast.CallExpr:
		// Check for Discord.bot(token) shorthand
		if dot, ok := e.Function.(*ast.DotExpr); ok {
			if ident, ok := dot.Object.(*ast.Identifier); ok && ident.Name == "Discord" && dot.Field == "bot" {
				tokenArg := "undefined"
				if len(e.Args) > 0 {
					tokenArg = g.genExpr(e.Args[0])
				}
				return fmt.Sprintf("(() => { const c = new Discord.Client({ intents: [Discord.GatewayIntentBits.Guilds, Discord.GatewayIntentBits.GuildMessages, Discord.GatewayIntentBits.MessageContent] }); c.__token = %s; process.nextTick(() => c.login(%s)); return c; })()", tokenArg, tokenArg)
			}
		}
		// Check for env("KEY") shorthand
		if ident, ok := e.Function.(*ast.Identifier); ok && ident.Name == "env" && len(e.Args) == 1 {
			return fmt.Sprintf("process.env[%s]", g.genExpr(e.Args[0]))
		}
		args := make([]string, len(e.Args))
		for i, a := range e.Args {
			args[i] = g.genExpr(a)
		}
		return fmt.Sprintf("%s(%s)", g.genExpr(e.Function), strings.Join(args, ", "))

	case *ast.DotExpr:
		return fmt.Sprintf("%s.%s", g.genExpr(e.Object), e.Field)

	case *ast.IndexExpr:
		return fmt.Sprintf("%s[%s]", g.genExpr(e.Object), g.genExpr(e.Index))

	case *ast.NewExpr:
		args := make([]string, len(e.Args))
		for i, a := range e.Args {
			args[i] = g.genExpr(a)
		}
		return fmt.Sprintf("new %s(%s)", e.ClassName, strings.Join(args, ", "))

	case *ast.AwaitExpr:
		return fmt.Sprintf("await %s", g.genExpr(e.Expr))

	case *ast.AwaitExpression:
		// "await all" or "await first" — handled contextually by parallel/race
		if ident, ok := e.Target.(*ast.Identifier); ok {
			if ident.Name == "all" || ident.Name == "first" {
				return fmt.Sprintf("await %s", ident.Name)
			}
		}
		return fmt.Sprintf("await __task_%s", g.genExpr(e.Target))

	case *ast.ReceiveExpression:
		return fmt.Sprintf("await %s.receive()", e.Channel)

	case *ast.NothingLiteral:
		return "null"

	case *ast.ObjectLiteral:
		if len(e.Keys) == 0 && len(e.ComputedProperties) == 0 {
			return "{}"
		}
		var pairs []string
		for i, key := range e.Keys {
			pairs = append(pairs, fmt.Sprintf("%s: %s", key, g.genExpr(e.Values[i])))
		}
		for _, cp := range e.ComputedProperties {
			pairs = append(pairs, fmt.Sprintf("[%s]: %s", g.genExpr(cp.KeyExpr), g.genExpr(cp.Value)))
		}
		return "{ " + strings.Join(pairs, ", ") + " }"

	case *ast.LambdaExpr:
		params := strings.Join(e.Params, ", ")
		if len(e.BodyStatements) > 0 {
			g.indent++
			body := g.genBlock(e.BodyStatements)
			g.indent--
			prefix := strings.Repeat("  ", g.indent)
			return fmt.Sprintf("(%s) => {\n%s%s}", params, body, prefix)
		}
		body := g.genExpr(e.Body)
		// Wrap object literals in parens so JS doesn't treat { as a block
		if _, isObj := e.Body.(*ast.ObjectLiteral); isObj {
			return fmt.Sprintf("(%s) => (%s)", params, body)
		}
		return fmt.Sprintf("(%s) => %s", params, body)

	case *ast.EmbedLiteral:
		return g.genEmbedLiteral(e)

	case *ast.SpreadExpr:
		return fmt.Sprintf("...%s", g.genExpr(e.Expr))

	case *ast.TypeCheckExpr:
		exprCode := g.genExpr(e.Expr)
		switch e.TypeName {
		case "text":
			return fmt.Sprintf("(typeof %s === \"string\")", exprCode)
		case "number":
			return fmt.Sprintf("(typeof %s === \"number\")", exprCode)
		case "boolean":
			return fmt.Sprintf("(typeof %s === \"boolean\")", exprCode)
		case "nothing":
			return fmt.Sprintf("(%s === null || %s === undefined)", exprCode, exprCode)
		default:
			return fmt.Sprintf("(typeof %s === \"%s\")", exprCode, e.TypeName)
		}

	case *ast.PropagateExpr:
		inner := g.genExpr(e.Expr)
		g.needsResultRuntime = true
		return fmt.Sprintf("__propagate(%s)", inner)

	case *ast.TryExpression:
		inner := g.genExpr(e.Expr)
		g.needsResultRuntime = true
		return fmt.Sprintf("__tryResult(%s)", inner)

	case *ast.ObjectMatchPattern:
		// This should not be reached directly in genExpr, handled in genMatch
		return "/* object match pattern */"

	case *ast.PipeExpr:
		// x | fn  becomes  fn(x)
		// x | fn(a, b)  becomes  fn(x, a, b)
		leftCode := g.genExpr(e.Left)
		switch right := e.Right.(type) {
		case *ast.CallExpr:
			// Insert left as first argument
			args := []string{leftCode}
			for _, a := range right.Args {
				args = append(args, g.genExpr(a))
			}
			return fmt.Sprintf("%s(%s)", g.genExpr(right.Function), strings.Join(args, ", "))
		case *ast.Identifier:
			return fmt.Sprintf("%s(%s)", right.Name, leftCode)
		default:
			return fmt.Sprintf("%s(%s)", g.genExpr(e.Right), leftCode)
		}

	case *ast.TaggedTemplateExpr:
		// Convert Quill {x} interpolations to JS ${x} in the template
		// Use the same smart conversion as regular string interpolation
		converted := convertInterpolation(e.Template)
		g.taggedTemplatesUsed = append(g.taggedTemplatesUsed, e.Tag)
		return fmt.Sprintf("%s`%s`", e.Tag, converted)

	case *ast.MockAssertionExpr:
		// This is handled at the ExpectStatement level, but provide a fallback
		return fmt.Sprintf("__mock_%s_calls.length === %d", e.FuncName, e.Count)

	case *ast.AskExpression:
		var out strings.Builder
		prompt := g.genExpr(e.Prompt)

		// Build options object
		optParts := []string{}
		if m, ok := e.Options["model"]; ok {
			optParts = append(optParts, fmt.Sprintf("model: %s", g.genExpr(m)))
		}
		if mt, ok := e.Options["max_tokens"]; ok {
			optParts = append(optParts, fmt.Sprintf("max_tokens: %s", g.genExpr(mt)))
		}
		if sys, ok := e.Options["system"]; ok {
			optParts = append(optParts, fmt.Sprintf("system: %s", g.genExpr(sys)))
		}
		if temp, ok := e.Options["temperature"]; ok {
			optParts = append(optParts, fmt.Sprintf("temperature: %s", g.genExpr(temp)))
		}
		opts := "{" + strings.Join(optParts, ", ") + "}"

		// Build structured output schema if present
		hasStructured := len(e.StructuredOutput) > 0
		schemaStr := ""
		if hasStructured {
			schemaParts := []string{}
			for k, v := range e.StructuredOutput {
				schemaParts = append(schemaParts, fmt.Sprintf(`"%s": "%s"`, k, v))
			}
			schemaStr = "{" + strings.Join(schemaParts, ", ") + "}"
		}

		switch e.Provider {
		case "openai":
			g.needsOpenAIRuntime = true
			out.WriteString(fmt.Sprintf("(await __ask_openai(%s, %s))", prompt, opts))
		case "gemini":
			g.needsGeminiRuntime = true
			out.WriteString(fmt.Sprintf("(await __ask_gemini(%s, %s))", prompt, opts))
		case "ollama":
			g.needsOllamaRuntime = true
			out.WriteString(fmt.Sprintf("(await __ask_ollama(%s, %s))", prompt, opts))
		default: // "claude"
			g.needsAIRuntime = true
			model := `"claude-sonnet-4-20250514"`
			maxTokens := "1024"
			if m, ok := e.Options["model"]; ok {
				model = g.genExpr(m)
			}
			if mt, ok := e.Options["max_tokens"]; ok {
				maxTokens = g.genExpr(mt)
			}
			if e.IsMessages {
				out.WriteString(fmt.Sprintf("(await (async () => { const __ai_resp = await __ai_client.messages.create({ model: %s, max_tokens: %s", model, maxTokens))
			} else {
				out.WriteString(fmt.Sprintf("(await (async () => { const __ai_resp = await __ai_client.messages.create({ model: %s, max_tokens: %s", model, maxTokens))
			}
			if sys, ok := e.Options["system"]; ok {
				out.WriteString(fmt.Sprintf(", system: %s", g.genExpr(sys)))
			}
			if temp, ok := e.Options["temperature"]; ok {
				out.WriteString(fmt.Sprintf(", temperature: %s", g.genExpr(temp)))
			}
			if e.IsMessages {
				out.WriteString(fmt.Sprintf(", messages: %s", prompt))
			} else {
				out.WriteString(fmt.Sprintf(", messages: [{ role: \"user\", content: %s }]", prompt))
			}
			out.WriteString(" }); return __ai_resp.content[0].text; })())")
		}

		// Wrap with structured output parsing if needed
		if hasStructured {
			g.needsStructuredOutput = true
			return fmt.Sprintf("__parse_structured(%s, %s)", out.String(), schemaStr)
		}
		return out.String()

	case *ast.EmbedExpression:
		g.needsVectorRuntime = true
		provider := e.Provider
		if provider == "" {
			provider = "openai"
		}
		model := e.Model
		if model == "" {
			return fmt.Sprintf("(await embed(%s, %q))", g.genExpr(e.Text), provider)
		}
		return fmt.Sprintf("(await embed(%s, %q, %q))", g.genExpr(e.Text), provider, model)

	default:
		return "undefined"
	}
}

