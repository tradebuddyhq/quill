// genWorkerRespond generates return new Response(...) for worker context.
func (g *Generator) genWorkerRespond(s *ast.RespondStatement, prefix string) string {
	expr := g.genExpr(s.Value)
	statusPart := ""
	if s.StatusCode > 0 {
		statusPart = fmt.Sprintf(", { status: %d }", s.StatusCode)
	}

	switch s.ContentType {
	case "json":
		headers := `{ headers: { "Content-Type": "application/json" }`
		if s.StatusCode > 0 {
			headers = fmt.Sprintf(`{ headers: { "Content-Type": "application/json" }, status: %d`, s.StatusCode)
		}
		return fmt.Sprintf("%sreturn new Response(JSON.stringify(%s), %s });", prefix, expr, headers)
	case "html":
		headers := `{ headers: { "Content-Type": "text/html" }`
		if s.StatusCode > 0 {
			headers = fmt.Sprintf(`{ headers: { "Content-Type": "text/html" }, status: %d`, s.StatusCode)
		}
		return fmt.Sprintf("%sreturn new Response(%s, %s });", prefix, expr, headers)
	default:
		if s.StatusCode > 0 {
			return fmt.Sprintf("%sreturn new Response(%s%s);", prefix, expr, statusPart)
		}
		return fmt.Sprintf("%sreturn new Response(%s);", prefix, expr)
	}
}

func escapeJS(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "\"", "\\\"")
	s = strings.ReplaceAll(s, "\n", "\\n")
	s = strings.ReplaceAll(s, "\r", "\\r")
	s = strings.ReplaceAll(s, "\t", "\\t")
	s = strings.ReplaceAll(s, "\x00", "\\0")
	return s
}

// replaceMyInInterpolations replaces "my." with "this." only inside {interpolation} braces.
func replaceMyInInterpolations(s string) string {
	var out strings.Builder
	i := 0
	for i < len(s) {
		if s[i] == '{' {
			// Find the matching closing brace
			j := i + 1
			for j < len(s) && s[j] != '}' {
				j++
			}
			if j < len(s) {
				content := s[i+1 : j]
				// Replace my. with this. only inside the interpolation
				content = strings.ReplaceAll(content, "my.", "this.")
				out.WriteByte('{')
				out.WriteString(content)
				out.WriteByte('}')
				i = j + 1
				continue
			}
		}
		out.WriteByte(s[i])
		i++
	}
	return out.String()
}

// convertInterpolation converts {expr} to ${expr} for JS template literals.
// Only converts {identifier} or {expr.field} patterns, not { css } blocks.
func convertInterpolation(s string) string {
	var out strings.Builder
	i := 0
	for i < len(s) {
		if s[i] == '{' {
			// Check if it's already ${
			if i > 0 && s[i-1] == '$' {
				out.WriteByte(s[i])
				i++
				continue
			}
			// Look ahead to see if content looks like an interpolation expression
			// (starts with a letter/underscore, contains only valid identifier chars, dots, parens, etc.)
			j := i + 1
			if j < len(s) && isInterpolationStart(s[j]) {
				// Find the closing brace
				for j < len(s) && s[j] != '}' {
					j++
				}
				if j < len(s) {
					content := s[i+1 : j]
					if isInterpolationExpr(content) {
						out.WriteString("${")
						out.WriteString(content)
						out.WriteByte('}')
						i = j + 1
						continue
					}
				}
			}
			// Not an interpolation — keep the brace as-is but escape for template literal
			out.WriteString("\\{")
			i++
		} else {
			out.WriteByte(s[i])
			i++
		}
	}
	return out.String()
}

// hasInterpolation returns true if the string contains at least one {identifier} interpolation.
func hasInterpolation(s string) bool {
	i := 0
	for i < len(s) {
		if s[i] == '{' {
			j := i + 1
			if j < len(s) && isInterpolationStart(s[j]) {
				for j < len(s) && s[j] != '}' {
					j++
				}
				if j < len(s) {
					content := s[i+1 : j]
					if isInterpolationExpr(content) {
						return true
					}
				}
			}
		}
		i++
	}
	return false
}

// isInterpolationStart returns true if the character can start a Quill interpolation.
func isInterpolationStart(c byte) bool {
	return (c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_'
}

// isInterpolationExpr returns true if the string looks like a valid interpolation expression
// (identifier, dot access, function call, etc.) rather than CSS or other content.
func isInterpolationExpr(s string) bool {
	if len(s) == 0 {
		return false
	}
	// Must start with a letter or underscore
	if !isInterpolationStart(s[0]) {
		return false
	}
	// Should only contain valid identifier characters, dots, parens, commas, spaces
	for _, c := range s {
		if !((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') ||
			c == '_' || c == '.' || c == '(' || c == ')' || c == ',' || c == ' ') {
			return false
		}
	}
	return true
}

