// genCommandRegistration generates the slash command registration and dispatch code.
func (g *Generator) genCommandRegistration(cmds []*ast.CommandStatement) string {
	var out strings.Builder

	clientVar := g.discordClientVar
	if clientVar == "" {
		clientVar = "client"
	}

	// Build commands array
	out.WriteString("const __quill_commands = [\n")
	for _, cmd := range cmds {
		out.WriteString(fmt.Sprintf("  new (require(\"discord.js\").SlashCommandBuilder)().setName(\"%s\").setDescription(\"%s\")", escapeJS(cmd.Name), escapeJS(cmd.Description)))
		for _, param := range cmd.Params {
			out.WriteString(fmt.Sprintf(".addStringOption(opt => opt.setName(\"%s\").setDescription(\"%s\").setRequired(true))", escapeJS(param), escapeJS(param)))
		}
		out.WriteString(",\n")
	}
	out.WriteString("];\n")

	// Ready handler to register commands
	out.WriteString(fmt.Sprintf("%s.on(\"ready\", async () => {\n", clientVar))
	out.WriteString("  const { REST, Routes } = require(\"discord.js\");\n")
	out.WriteString(fmt.Sprintf("  const rest = new REST({ version: \"10\" }).setToken(%s.token);\n", clientVar))
	out.WriteString("  try {\n")
	out.WriteString(fmt.Sprintf("    await rest.put(Routes.applicationCommands(%s.user.id), { body: __quill_commands.map(c => c.toJSON()) });\n", clientVar))
	out.WriteString("    console.log(\"Slash commands registered.\");\n")
	out.WriteString("  } catch(e) { console.error(\"Failed to register commands:\", e); }\n")
	out.WriteString("});\n")

	// Interaction handler
	out.WriteString(fmt.Sprintf("%s.on(\"interactionCreate\", async (interaction) => {\n", clientVar))
	out.WriteString("  if (!interaction.isChatInputCommand()) return;\n")
	for i, cmd := range cmds {
		keyword := "if"
		if i > 0 {
			keyword = "else if"
		}
		out.WriteString(fmt.Sprintf("  %s (interaction.commandName === \"%s\") {\n", keyword, escapeJS(cmd.Name)))
		// Extract params
		for _, param := range cmd.Params {
			out.WriteString(fmt.Sprintf("    const %s = interaction.options.getString(\"%s\");\n", param, escapeJS(param)))
		}
		// Generate body
		g.indent += 2
		for _, stmt := range cmd.Body {
			out.WriteString(g.genStmt(stmt))
			out.WriteString("\n")
		}
		g.indent -= 2
		out.WriteString("  }\n")
	}
	out.WriteString("});\n")

	return out.String()
}

